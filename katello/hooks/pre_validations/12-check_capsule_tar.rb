certs_tar = param('certs', 'tar_file')

if certs_tar.value
  certs_tar.value = File.expand_path(certs_tar.value)
  if param('certs', 'deploy').value && !File.file?(certs_tar.value)
    fail_and_exit("The certs tar file generated by the server is not present at #{certs_tar.value}, exiting.", 101)
  end
end
